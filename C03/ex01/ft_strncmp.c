int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int	result;
	int	i;

	i = 0;
	if (n == 0)
		return (0);
	while (s1[i] != '\0' && s1[i] == s2[i] && (unsigned int)i < (n - 1))
	{
		i++;
	}
	result = s1[i] - s2[i];
	return (result);
}
